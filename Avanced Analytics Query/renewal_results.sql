SELECT 
IN_ACCOUNT_NUMBER,
'renewal_results' AS MODEL_NAME,
SNAPSHOT_DATE AS LAST_MODEL_REFRESH_DATE,
non_renewal_probability AS SCORE,
decile AS DECILE
FROM(
SELECT 
*,
ROW_NUMBER() OVER (PARTITION BY IN_ACCOUNT_NUMBER) AS IN_RANK
FROM(
SELECT 
*,
RANK() OVER (ORDER BY SNAPSHOT_DATE DESC) AS RNEW_LAST_DATE_FLAG
FROM neocognix.renewal_results
WHERE SNAPSHOT_DATE < date('"+context.vLoadDate+"')
)RNEW_LAST_DATE
WHERE RNEW_LAST_DATE_FLAG=1
)UNI_IN WHERE IN_RANK=1;


